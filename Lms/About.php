        <i><p style="font-size:25px; color:white">The University of Engineering & Technology Mardan (UET Mardan) was established on 7th March, 2018 by the Government of Khyber Pakhtunkhwa under the KP Universities Act. The University Campus is situated on main Charsada Road, Mardan spreading over an area of 172 Kanals. Previously, the Campus had been operational since 2002 as one of the satellite campuses of UET Peshawar. Initially, two teaching departments, i.e., Telecommunication Engineering and Computer Software Engineering were established with BSc programs. On the basis of public interest due to its location and available facilities, the campus showed fast pace growth in all aspects including infrastructure, laboratories, and faculty. In addition to MSc and PhD programs in the afore mentioned two departments, Department of Electrical Engineering was established in 2012 offering BSc Electrical Engineering in Power as well as Communication streams. Presently, about 1000 students, studying at Undergraduate and Postgraduate levels, are enrolled at UET Mardan. In 2016, Higher Education Commission (HEC), Pakistan approved a grant of Rs. 1.336 billion for the establishment of Civil, Electrical and Mechanical Engineering Departments and to enhance facilities for the students and faculty members in the campus. This approval acts as an endorsement by the HEC on viability of the campus to function as full-fledged engineering university.</p></i>
